package com.refactor.bootcamp.long_parameter_list.example_2;

public class DaysTempRange {

    private int low;
    private int high;

    public DaysTempRange(int low, int high) {
        this.low = low;
        this.high = high;
    }

    public int getLow() {
        return low;
    }

    public int getHigh() {
        return high;
    }
}
